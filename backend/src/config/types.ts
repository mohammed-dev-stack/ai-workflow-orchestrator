/**
 * config/types.ts
 *
 * أنواع مشتركة بين أكثر من ملف إعداد واحد. وُجد هذا الملف لسبب واحد
 * ملموس: HealthCheckResult كان مُعرَّفًا مرتين بشكل متطابق حرفيًا —
 * نسخة داخل database.config.ts ونسخة داخل redis.config.ts. أي تعديل
 * مستقبلي (إضافة حقل جديد لنتيجة الفحص مثلاً) كان يتطلب تذكّر تعديل
 * مكانين منفصلين بدل واحد. هذا الملف هو الحل الجذري لذلك، لا ترقيع.
 */
 
export interface HealthCheckResult {
  /**
   * حالة فحص الخدمة:
   * - 'connected'    اتصال قائم واستجاب فعليًا (ping نجح فعلاً، وليس
   *                   مجرد وجود كائن اتصال).
   * - 'disconnected' لا يوجد اتصال نشط أصلاً — إما لم تُستدعَ التهيئة
   *                   بعد، أو أُغلق الاتصال عمدًا (graceful shutdown).
   * - 'error'        كان هناك اتصال، لكن محاولة التحقق من استجابته
   *                   (ping) فشلت. هذه حالة مختلفة جوهريًا عن
   *                   'disconnected': هنا قد تكون الخدمة متصلة اسميًا
   *                   لكنها لا تستجيب — إشارة مبكرة لمشكلة شبكة أو
   *                   انقطاع وشيك يستحق تنبيهًا فوريًا لفريق المراقبة،
   *                   بخلاف 'disconnected' التي قد تكون متوقعة تمامًا
   *                   (مثل حالة ما قبل الإقلاع).
   */
  status: 'connected' | 'disconnected' | 'error';
 
  /**
   * زمن استجابة فحص الـ ping، بالميلي ثانية (ms). موجود فقط عندما
   * status === 'connected'. غيابه في أي حالة أخرى متعمَّد، لا سهو.
   */
  latency?: number;
 
  /**
   * رسالة الخطأ الفعلية من المكتبة المستخدمة (mongoose/ioredis).
   * موجودة فقط عندما status === 'error'.
   */
  error?: string;
}